package id.web.dedekurniawan.exception;

public class NumeralConvertionException extends Exception{
    public NumeralConvertionException() { super(); }
    public NumeralConvertionException(String message) { super(message); }
    public NumeralConvertionException(String message, Throwable cause) { super(message, cause); }
    public NumeralConvertionException(Throwable cause) { super(cause); }
}
